package us.coffeecode.advent_of_code.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a Java class as being a solution to an Advent of Code puzzle. Objects marked with this annotation represent a
 * single day during an Advent of Code event.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface AdventOfCodeSolution {

  int year();

  int day();
}
